    ADD CONSTRAINT kb_sources_type_check CHECK (source_type IN ('pdf_upload', 'web_url'));

ALTER TABLE kb_sources DROP CONSTRAINT IF EXISTS kb_sources_status_check;
UPDATE kb_sources
SET status = 'error'
WHERE status NOT IN ('pending', 'ready', 'syncing', 'error', 'disabled');
ALTER TABLE kb_sources
