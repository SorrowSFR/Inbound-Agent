    source_type    text NOT NULL,
    title          text NOT NULL DEFAULT '',
    source_url     text,
    raw_text       text,
    storage_bucket text,
    storage_path   text,
    mime_type      text,
    checksum       text,
    status         text NOT NULL DEFAULT 'pending',
    enabled        boolean NOT NULL DEFAULT true,
    last_synced_at timestamptz,
    sync_error     text,
    metadata       jsonb NOT NULL DEFAULT '{}'::jsonb
